package com.imooc;

public class StringDemo {

	public static void main(String[] args) {
		
		String a = "";
		System.out.println("a=" + a);
		
		String b = "Hello";
		System.out.println("b=" + b);
		
		String c = "A\u005d\u005dB";
		System.out.println("c=" + c);
		
		String d = "Hello World";
		System.out.println("d=" + d);		
		
	}

}
