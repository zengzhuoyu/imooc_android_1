package com.imooc;

public class CharDemo {

	public static void main(String[] args) {
		char a = 'a';
		System.out.println("a=" + a);
		
		char b = 65;
		System.out.println("b=" + b);
		
		char c = 65535;
		System.out.println("c=" + c);	
		
		char d = (char)65536;
		System.out.println("d=" + d);	
		
		char e = '\u005d';
		System.out.println("e=" + e);	
	}

}
